#!/bin/bash
################################################################################
# Функция "minor_exec_io_clfs"
# Version: 0.1

minor_exec_io_clfs ()
{
local actions=${1:-OFF}

case "${actions}" in
	OK)
		exec 6>&1 7>&2 8<&0
	;;
	OFF)
		exec 1>&6 2>&7 0<&8
		exec 6>&- 7>&- 8<&-
	;;
esac
}

